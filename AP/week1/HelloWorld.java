public class HelloWorld
{
     /* This class demonstrates the use of the println()
     method to print the message "Hello, world!" */
     public static void main(String[] args)
     {
          System.out.println("Hello, world!");
          System.out.println();    // println() without an argument will print a blank line
          System.out.print("test");  // print() will appened the String to whatever came before it
     }                               // ...rather than stick it on a new line
}
